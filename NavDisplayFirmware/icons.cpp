// =====================================================
// GET ICON FUNCTION
// =====================================================

const uint8_t* getIcon(const char* name)
{

    for(uint16_t i=0; i<ICON_COUNT; i++)
    {

        if(strcmp(name, iconTable[i].name)==0)
        {
            return iconTable[i].bitmap;
        }

    }


    return nullptr;

}
